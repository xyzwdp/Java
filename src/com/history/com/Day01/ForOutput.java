package com.history.com.Day01;

public class ForOutput {
    public static void main(String[] args){
        //输出1-5数字
        for (int i=1;i<=5;i++){
            System.out.println(i);
        }

        System.out.println("-------------");

        //输出5-1数字
        for (int i=5;i>=1;i--){
            System.out.println(i);
        }
    }
}
