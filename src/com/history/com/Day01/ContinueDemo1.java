package com.history.com.Day01;

public class ContinueDemo1 {
    public static void main(String[] args){
        //continue
        //break
        for (int i = 1;i <=5;i++){
            if (i%2 == 0){
                //continue;
                break;
            }
            System.out.println(i);
        }

    }
}
