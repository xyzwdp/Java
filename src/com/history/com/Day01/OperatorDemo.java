package com.history.com.Day01;

public class OperatorDemo {
    public static void main(String[] args){
        //定义两个变量
        int a=6;
        int b=4;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        //取商，整数相除只能得到整数
        System.out.println("-----------------");
        System.out.println(a/b);
        //取余
        System.out.println(a%b);

        float c=10.0f;
        double d=20;
        System.out.println(c/b);
        System.out.println(d/b);
    }
}
