package com.history.com.Day01;

//long与float类型数值后面分别需要加l与f。

public class VariableDemo1 {
    public static void main(String[] args){
        //定义byte（8）类型变量
        byte a=10;
        System.out.println(a);

        //定义short（16）类型变量
        short b=200;
        System.out.println(b);

        //定义int（32）类型变量
        int c=1100;
        System.out.println(c);

        //定义long（64）类型变量
        long d=2200000000l;
        long x=9999999999999l;
        System.out.println(d);

        //定义float（32）类型变量
        float e=3.14f;
        //float y= 1.11111111111111;
        //System.out.println(y);
        System.out.println(e);

        //定义double（64）类型变量
        double f=3.145;
        System.out.println(f);

        //定义char类型变量
        char g='a';
        System.out.println(g);

        //定义boolean类型变量
        boolean h=false;
        System.out.println(h);

        //定义


    }
}
